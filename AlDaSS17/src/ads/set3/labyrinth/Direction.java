package ads.set3.labyrinth;

public enum Direction {
	/**
	 * Movement towards north (decreasing y-coordinate)
	 */
	NORTH,

	/**
	 * Movement towards east (increasing x-coordinate)
	 */
	EAST,

	/**
	 * Movement towards south (increasing y-coordinate)
	 */
	SOUTH,

	/**
	 * Movement towards west (decreasing x-coordinate)
	 */
	WEST
}
